#include <stdio.h>

int main() {
    char ch;
    int vowel_count = 0;

    // รับข้อความจากผู้ใช้ทีละตัวอักษร
    printf("กรุณาป้อนข้อความภาษาอังกฤษ (กด Enter เพื่อจบการป้อน): ");

    while ((ch = getchar()) != '\n') { // รับตัวอักษรทีละตัวจนกว่าจะเจอ '\n'
        // ตรวจสอบว่าตัวอักษรเป็นสระหรือไม่ (ทั้งตัวพิมพ์เล็กและตัวพิมพ์ใหญ่)
        if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' ||
            ch == 'A' || ch == 'E' || ch == 'I' || ch == 'O' || ch == 'U') {
            vowel_count++; // เพิ่มจำนวนสระ
        }
    }

    // แสดงจำนวนสระ
    printf("จำนวนสระในข้อความคือ: %d\n", vowel_count);

    return 0;
}
