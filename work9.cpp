#include <stdio.h>

int main() {
    int score1, score2, totalScore;
    char grade;

    // ????????????????????
    printf("Enter the first score (out of 50): ");
    scanf("%d", &score1);

    // ??????????????????????????????????????????
    if (score1 < 0 || score1 > 50) {
        printf("Invalid score! Please enter a score between 0 and 50.\n");
        return 1;
    }

    printf("Enter the second score (out of 50): ");
    scanf("%d", &score2);

    // ??????????????????????????????????????????
    if (score2 < 0 || score2 > 50) {
        printf("Invalid score! Please enter a score between 0 and 50.\n");
        return 1;
    }

    // ?????????????
    totalScore = score1 + score2;

    // ???????????
    if (totalScore >= 80) {
        grade = 'A';
    } else if (totalScore >= 75) {
        grade = 'B';
    } else if (totalScore >= 70) {
        grade = 'C';
    } else if (totalScore >= 60) {
        grade = 'D';
    } else if (totalScore >= 50) {
        grade = 'E';
    } else {
        grade = 'F';
    }

    // ???????????
    printf("Total Score: %d\n", totalScore);
    printf("Grade: %c\n", grade);

    return 0;
}

